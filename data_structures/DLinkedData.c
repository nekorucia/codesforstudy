#include <stdio.h>
#include <stdlib.h>

typedef struct _node
{
    int data;
    struct _node* next;
} Node;

int main(void)
{
    Node* head = NULL;
    Node* tail = NULL;
    Node* cur = NULL;

    Node* newnode = NULL;
    int readData;

    // 데이터 입력 과정

    head=(Node*)malloc(sizeof(Node));
    tail=head;

    while(1)
    {
        printf("자연수 입력 : ");
        scanf("%d",&readData);
        if(readData<1)
            break;

        // 노드 추가과정
        newnode = (Node*)malloc(sizeof(Node));
        newnode->data=readData;
        newnode->next=NULL;
        /*
        if(head==NULL)
        {
            head=newnode;
        }
        else 
        {
            tail->next=head;
        }
        */
        tail->next=newnode;

        tail=newnode;
    }
    printf("\n");

    //입력받은 데이터의 출력과정
    printf("입력받은 데이터 전체출력 \n");
    if(head==NULL)
        printf("저장된 데이터 없음 \n");
    else 
    {
        cur = head;
        //printf("%d ",cur->data);

        while(cur->next!=NULL)
        {
            cur=cur->next;
            printf("%d ",cur->data);
        }
    }
    printf("\n");

    //메모리 해제과정
    if(head==NULL)
    {
        return 0;
    }
    else
    {
        Node* delNode = head;
        Node* delNextNode = head->next;

        //printf("%d를 삭제합니다 \n",head->data);
        //free(delNode);

        while(delNextNode!=NULL)
        {
            delNode=delNextNode;
            delNextNode=delNextNode->next;

            printf("%d를 삭제합니다\n",delNode->data);
            free(delNode);
        }
    }
    
    return 0;
}
